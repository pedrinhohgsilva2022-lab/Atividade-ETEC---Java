public class CombinacaoOperadores {
    public static void main(String[] args) {
        int a = 3;
        int b = 6;
        int c = 9;
        int d = 12;
        int e = 15;

        a += 1;
        b -= 2;
        c *= 3;
        d /= 4;
        e %= 5;

        System.out.println("O valor de 'a': " + a + ", e o valor de 'b': " + b);
        System.out.println("O valor de 'c': " + c + ", o valor de 'd': " + d + " e por ultimo, o valor de 'e': " + e);

    }
}
